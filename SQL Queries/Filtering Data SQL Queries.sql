

Here are some SQL queries for filtering data:

1. Film title contains the word Uptown in any position

SELECT film_ID, title, description FROM film
WHERE title
LIKE '%Uptown%'

2. Film length is more than 120 minutes and rental rate is more than 2.99

SELECT film_ID, title, description, length, rental_rate
FROM film
WHERE length > 120
AND rental_rate > 2.99

3. Rental duration is between 3 and 7 days (where 3 and 7 arenâ€™t inclusive)

SELECT film_ID, title, description, rental_duration
FROM film
WHERE rental_duration > 3 and rental_duration < 7

4. Film replacement cost is less than 14.99

SELECT film_ID, title, description, replacement_cost
FROM film
WHERE replacement_cost < 14.99

5. Film rating is either PG or G

SELECT film_ID, title, description, rating FROM film
WHERE rating IN ('PG',
G')

6. The inventory team has asked for the following information about this list:
a. Count of the movies
b. Average rental rate
c. Maximum rental duration and minimum rental duration

SELECT rating, COUNT(film_id),
AVG(rental_rate), MAX(rental_duration), MIN(rental_duration) FROM film
WHERE rating IN ('PG', 'G')
GROUP BY rating

